first_num = input('enter fisrt num')
second_num = input('enter second num')
# If you have a string variable containing number
# And you want treat it as a number
# You must convert it to a numberic datatype
# int() converts a string to an integer e.g. 4, 5, 100
print(int(first_num) + int(second_num))

# float() converts a string to a decimal or float e.g. 1.0, 2.3, 3.1415
print(float(first_num) + float(second_num))
